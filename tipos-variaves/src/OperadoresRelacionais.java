public class OperadoresRelacionais {

    public static void main(String[] args) {
        
        int numero1 = 1;
        int numero2 = 3;

        boolean simNao = numero1 == numero2;

        if(simNao == true)
            System.out.println(" Os valores comparados são iguais   ");
        else
            System.out.println(" Os valores comparados são diferentes  ");

    }

}
